    let indiceActual = 0;
    let juegosActuales = juegos; // Arreglo de juegos actualmente cargado en el carrusel
    
    function cargarDatosTarjeta(contenedor, datos) {
        const contenedorElemento = document.querySelector(`.${contenedor} .contenido`);
        const imagenElemento = contenedorElemento.querySelector(".tarjetaJuego .imagen img");
        const nombreElemento = contenedorElemento.querySelector(".tarjetaJuego .data_Juego h2");
        const generoElemento = contenedorElemento.querySelector(".tarjetaJuego .data_Juego .descrip:nth-child(2)");
        const plataformasElemento = contenedorElemento.querySelector(".tarjetaJuego .data_Juego .descrip:nth-child(3)");
        const lanzamientoElemento = contenedorElemento.querySelector(".tarjetaJuego .data_Juego .descrip:nth-child(4)");
        const desarrolladorElemento = contenedorElemento.querySelector(".tarjetaJuego .data_Juego .descrip:nth-child(5)");
    
        imagenElemento.src = datos.imagen;
        nombreElemento.textContent = datos.nombre;
        generoElemento.textContent = datos.genero;
        plataformasElemento.innerHTML = "";
        datos.plataformas.forEach(plataforma => {
            const imgElemento = document.createElement("img");
            imgElemento.src = plataforma;
            imgElemento.alt = "";
            plataformasElemento.appendChild(imgElemento);
        });
        lanzamientoElemento.textContent = datos.lanzamiento;
        desarrolladorElemento.textContent = datos.desarrollador;
    }
    
    function cargarDatosCarrusel(juegos) {
        juegosActuales = juegos; // Actualizar el arreglo de juegos actual
        indiceActual = 0; // Reiniciar el índice al cargar nuevos datos
        cargarDatosTarjeta("contenedor1", juegosActuales[indiceActual]);
        cargarDatosTarjeta("contenedor2", juegosActuales[(indiceActual + 1) % juegosActuales.length]);
        cargarDatosTarjeta("contenedor3", juegosActuales[(indiceActual + 2) % juegosActuales.length]);
    }
    
    // Cargar los datos de la tarjeta inicial
    cargarDatosTarjeta("contenedor1", juegosActuales[indiceActual]);
    cargarDatosTarjeta("contenedor2", juegosActuales[(indiceActual + 1) % juegosActuales.length]);
    cargarDatosTarjeta("contenedor3", juegosActuales[(indiceActual + 2) % juegosActuales.length]);
    
    const prevLink = document.querySelector('.slider .prev');
    const nextLink = document.querySelector('.slider .next');
    
    prevLink.addEventListener('click', retrocederSlider);
    nextLink.addEventListener('click', avanzarSlider);
    
    function retrocederSlider(event) {
        event.preventDefault();
    
        indiceActual = (indiceActual - 1 + juegosActuales.length) % juegosActuales.length;
        cargarDatosTarjeta("contenedor1", juegosActuales[indiceActual]);
        cargarDatosTarjeta("contenedor2", juegosActuales[(indiceActual + 1) % juegosActuales.length]);
        cargarDatosTarjeta("contenedor3", juegosActuales[(indiceActual + 2) % juegosActuales.length]);
    }
    
    function avanzarSlider(event) {
        event.preventDefault();
    
        indiceActual = (indiceActual + 1) % juegosActuales.length;
        cargarDatosTarjeta("contenedor1", juegosActuales[indiceActual]);
        cargarDatosTarjeta("contenedor2", juegosActuales[(indiceActual + 1) % juegosActuales.length]);
        cargarDatosTarjeta("contenedor3", juegosActuales[(indiceActual + 2) % juegosActuales.length]);
    }
    
